function add(a, b) {
  return a + b;
}

function subtracts(a, b) {
  return b - a;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function increments(n) {
  return n++;
}

function decrements(n) {
  return n--;
}

function parses(n) {
  return parseInt(n, 10);
}

function preserveDecimal(n) {
  return parseFloat(n);
}
